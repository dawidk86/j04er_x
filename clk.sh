#!/bin/bash

# Kali Color Palette
RED='\033[0;31m'
GREEN='\033[0;32m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
YELLOW='\033[1;33m'
BOLD='\033[1m'
NC='\033[0m' # No Color

# Hide Cursor for cleaner look
tput civis

# Trap Ctrl+C to restore cursor and exit cleanly
trap "tput cnorm; clear; exit" SIGINT

# Setup Weather Cache Variables
WEATHER_CACHE="/tmp/weather_cache.txt"
LAST_CHECK=0
CHECK_INTERVAL=900 # Update weather every 900 seconds (15 mins)

while true; do
    clear

    # --- 1. ASCII Art Time ---
    echo -e "${BLUE}"
    date +"%H : %M : %S" | figlet -f slant
    echo -e "${NC}"

    # --- 2. Date Information ---
    echo -e "   ${RED}${BOLD}DATE:${NC}   $(date +"%A, %d %B %Y")"

    # --- 3. System Info ---
    echo -e "   ${YELLOW}${BOLD}USER:${NC}   $(whoami)@$(hostname)"

    echo -e "${BLUE}---------------------------------------------${NC}"

    # --- 4. Weather Logic (with caching) ---
    # Calculate time since last update
    NOW=$(date +%s)
    TIME_DIFF=$((NOW - LAST_CHECK))

    # Only run curl if the cache file is missing OR it's been > 15 mins
    if [ ! -f "$WEATHER_CACHE" ] || [ "$TIME_DIFF" -ge "$CHECK_INTERVAL" ]; then
        # Fetch weather quietly in background to file
        # -s = silent, -m 3 = 3 sec timeout
        # using ?format=4 for a reliable one-line summary, or ?0Q for block
        CURRENT_WEATHER=$(curl -s -m 3 "wttr.in?0Q")
        
        if [ ! -z "$CURRENT_WEATHER" ]; then
            echo "$CURRENT_WEATHER" > "$WEATHER_CACHE"
            LAST_CHECK=$NOW
        fi
    fi

    echo -e "   ${GREEN}${BOLD}WEATHER (Updates every 15m):${NC}"
    echo ""

    if [ -f "$WEATHER_CACHE" ]; then
        # Read from cache file and indent
        cat "$WEATHER_CACHE" | sed 's/^/   /'
    else
        echo -e "   ${RED}Fetching weather data...${NC}"
    fi

    echo ""
    echo -e "${CYAN}Press [Ctrl+C] to exit${NC}"

    # Wait 1 second before refreshing the clock
    sleep 1
done
EOF

